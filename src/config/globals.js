import nodemailer from 'nodemailer'
import dotenv from 'dotenv'

dotenv.config()

export const MAIL_TRANSPORTER = nodemailer.createTransport({
  host: 'smtp.gmail.com',
  port: 587,
  secure: false,
  auth: {
    user: process.env.EMAIL_USER,
    pass: process.env.EMAIL_PASS,
  },
  tls: {
    rejectUnauthorized: false,
  },
})
